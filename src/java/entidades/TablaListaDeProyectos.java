package entidades;

public class TablaListaDeProyectos {

    private String ProyectosyConceptos;
    private String Productos;
    private String Facturable;
    private String Especialidad;

    public String getProyectosyConceptos() {
        return ProyectosyConceptos;
    }

    public void setProyectosyConceptos(String ProyectosyConceptos) {
        this.ProyectosyConceptos = ProyectosyConceptos;
    }

    public String getProductos() {
        return Productos;
    }

    public void setProductos(String Productos) {
        this.Productos = Productos;
    }

    public String getFacturable() {
        return Facturable;
    }

    public void setFacturable(String Facturable) {
        this.Facturable = Facturable;
    }

    public String getEspecialidad() {
        return Especialidad;
    }

    public void setEspecialidad(String Especialidad) {
        this.Especialidad = Especialidad;
    }

}
